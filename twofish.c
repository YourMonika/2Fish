/* Зашифрование */
void encrypt(const UINT K[40], UINT S[4][256], BYTE PT[16])
{
    UINT R0, R1, R2, R3;
    UINT T0, T1;

    /* Входное отбеливвание */
    R3 = K[3] ^ BSWAP(((UINT*)PT)[3]);
    R2 = K[2] ^ BSWAP(((UINT*)PT)[2]);
    R1 = K[1] ^ BSWAP(((UINT*)PT)[1]);
    R0 = K[0] ^ BSWAP(((UINT*)PT)[0]);

    printf("WHITENING FINISHED\nENCRYPTION STARTED \n\n");
    for(int i = 0; i < 16; i++){
        if(i%2==0){
            ENC_ROUND(R0, R1, R2, R3, i);
        } else{
            ENC_ROUND(R2, R3, R0, R1, i);
        }
    }


    printf("\nENCRYPTION FINISHED.\n WHITENING...\n");
    /* Выходное отбеливание  */
    ((UINT*)PT)[3] = BSWAP(R1 ^ K[7]);
    ((UINT*)PT)[2] = BSWAP(R0 ^ K[6]);
    ((UINT*)PT)[1] = BSWAP(R3 ^ K[5]);
    ((UINT*)PT)[0] = BSWAP(R2 ^ K[4]);
    printf("WHITENING FINISHED\n\n");


}

/* Круг дешифровки */
#define DEC_ROUND(R0, R1, R2, R3, round) \
    T0 = full_S_g(R0); \
    T1 = full_S_g(ROL(R1, 8)); \
    R2 = ROL(R2, 1) ^ (T0 + T1 + K[2*round+8]); \
    R3 = ROR(R3 ^ (T0 + 2*T1 + K[2*round+9]), 1); \
    printf("---rnd = %u, R0=%x, R1=%x, R2=%x, R3=%x \n", (15 - round), R0, R1, R2, R3);

void decrypt(const UINT K[40], UINT S[4][256], BYTE PT[16])
{
    UINT T0, T1;
    UINT R0, R1, R2, R3;

    /* Входное(обратно выходное) отбеливание*/
    R3 = K[7] ^ BSWAP(((UINT*)PT)[3]);
    R2 = K[6] ^ BSWAP(((UINT*)PT)[2]);
    R1 = K[5] ^ BSWAP(((UINT*)PT)[1]);
    R0 = K[4] ^ BSWAP(((UINT*)PT)[0]);
    printf("WHITENING FINISHED\nDECRYPTION STARTED \n\n");
    for(int i = 15; i>= 0; i--){
        if(i%2==1){
            DEC_ROUND(R0, R1, R2, R3, i);
        } else{
            DEC_ROUND(R2, R3, R0, R1, i);
        }
    }

    printf("\nDECRYPTION FINISHED. \nWHITENING...\n");
    /* Выходное (обратно входное) отбеливание */
    ((UINT*)PT)[3] = BSWAP(R1 ^ K[3]);
    ((UINT*)PT)[2] = BSWAP(R0 ^ K[2]);
    ((UINT*)PT)[1] = BSWAP(R3 ^ K[1]);
    ((UINT*)PT)[0] = BSWAP(R2 ^ K[0]);
    printf("WHITENING FINISHED\n\n");
}

/* Заполнение KeyShedule структуры */
void keySchedule(BYTE M[], int N, UINT **S, UINT K[40], int *k)
{
    UINT Mo[4], Me[4];
    int i, j;
    BYTE vector[8];
    UINT A, B;

    *k = (N + 63) / 64; /*Если блок меньше 128, 196, 256*/
    *S = (UINT*)malloc(sizeof(UINT) * (*k));

    for (i = 0; i < *k; i++)
    {
        Me[i] = BSWAP(((UINT*)M)[2*i]);
        Mo[i] = BSWAP(((UINT*)M)[2*i+1]);
    }

    for (i = 0; i < *k; i++)
    {
        for (j = 0; j < 4; j++) vector[j] = _b(Me[i], j);
        for (j = 0; j < 4; j++) vector[j+4] = _b(Mo[i], j);
        (*S)[(*k)-i-1] = RSMmul(vector);
    }

    for (i = 0; i < 20; i++)
    {

        A = g(2*i*RHO, Me, *k);
        B = ROL(g(2*i*RHO + RHO, Mo, *k), 8);
        K[2*i] = A+B;
        K[2*i+1] = ROL(A + 2*B, 9);
    }
}

