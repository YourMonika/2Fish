/* Безключевая h функция ( для развертки ключей ) */
UINT g(UINT X, UINT L[4], int k)
{
    BYTE y0, y1, y2, y3;
    BYTE z0, z1, z2, z3;
    y0 = b0(X);
    y1 = b1(X);
    y2 = b2(X);
    y3 = b3(X);

    switch(k)
    {
        case 4:
            y0 = Q1[0][y0] ^ b0(L[3]);
            y1 = Q0[1][y1] ^ b1(L[3]);
            y2 = Q0[2][y2] ^ b2(L[3]);
            y3 = Q1[3][y3] ^ b3(L[3]);
        case 3:
            y0 = Q1[0][y0] ^ b0(L[2]);
            y1 = Q1[1][y1] ^ b1(L[2]);
            y2 = Q0[2][y2] ^ b2(L[2]);
            y3 = Q0[3][y3] ^ b3(L[2]);
        case 2:
            y0 = Q1[0][  Q0 [0][ Q0[0][y0] ^ b0(L[0]) ] ^ b0(L[1]) ];
            y1 = Q0[1][  Q0 [1][ Q1[1][y1] ^ b1(L[0]) ] ^ b1(L[1]) ];
            y2 = Q1[2][  Q1 [2][ Q0[2][y2] ^ b2(L[0]) ] ^ b2(L[1]) ];
            y3 = Q0[3][  Q1 [3][ Q1[3][y3] ^ b3(L[0]) ] ^ b3(L[1]) ];
    }

    /* мгновенно вычисляем вектор Z(z0, z1, z2, z3) без умножения на MDS */
    z0 = multEF[y0] ^ y1 ^         multEF[y2] ^ mult5B[y3];
    z1 = multEF[y0] ^ mult5B[y1] ^ y2 ^         multEF[y3];
    z2 = mult5B[y0] ^ multEF[y1] ^ multEF[y2] ^         y3;
    z3 = y0 ^         multEF[y1] ^ mult5B[y2] ^ mult5B[y3];

    return BYTES_TO_UINT(z0, z1, z2, z3);
}

/* Заполняем QF для любого входа - одна прогонка, полного ключевого множества
 * Чтобы потом за 1 выполнять все 16 раундов и не просчитывать каждый раз*/
void fullKey(UINT L[4], int k, UINT QF[4][256])
{
    BYTE y0, y1, y2, y3;

    int i;

    /* Для любого входного значения прогоняем по S-box и умножаем на MDS */
    for (i=0; i<256; i++)
    {
        /* run the Q permutations */
        y0 = i; y1=i; y2=i; y3=i;
        switch(k)
        {
            case 4:
                y0 = Q1[0][y0] ^ b0(L[3]);
                y1 = Q0[1][y1] ^ b1(L[3]);
                y2 = Q0[2][y2] ^ b2(L[3]);
                y3 = Q1[3][y3] ^ b3(L[3]);
            case 3:
                y0 = Q1[0][y0] ^ b0(L[2]);
                y1 = Q1[1][y1] ^ b1(L[2]);
                y2 = Q0[2][y2] ^ b2(L[2]);
                y3 = Q0[3][y3] ^ b3(L[2]);
            case 2:
                y0 = Q1[0][  Q0 [0][ Q0[0][y0] ^ b0(L[0]) ] ^ b0(L[1]) ];
                y1 = Q0[1][  Q0 [1][ Q1[1][y1] ^ b1(L[0]) ] ^ b1(L[1]) ];
                y2 = Q1[2][  Q1 [2][ Q0[2][y2] ^ b2(L[0]) ] ^ b2(L[1]) ];
                y3 = Q0[3][  Q1 [3][ Q1[3][y3] ^ b3(L[0]) ] ^ b3(L[1]) ];
        }

        /* умножение с матрицей MDS (частичное), суть не меняется
         * То же самое что и раньше*/
        QF[0][i] = ((multEF[y0] << 24) | (multEF[y0] << 16) | (mult5B[y0] << 8) | y0);
        QF[1][i] = ((y1 << 24) | (mult5B[y1] << 16) | (multEF[y1] << 8) | multEF[y1]);
        QF[2][i] = ((multEF[y2] << 24) | (y2 << 16) | (multEF[y2] << 8) | mult5B[y2]);
        QF[3][i] = ((mult5B[y3] << 24) | (multEF[y3] << 16) | (y3 << 8) | mult5B[y3]);
    }
}


