#define ROL(x,n) (((x) << ((n) & 0x1F)) | ((x) >> (32-((n) & 0x1F))))
#define ROR(x,n) (((x) >> ((n) & 0x1F)) | ((x) << (32-((n) & 0x1F))))

#if BIG_ENDIAN == 1
#define BSWAP(x) (((ROR(x,8) & 0xFF00FF00) | (ROL(x,8) & 0x00FF00FF)))
#else
#define BSWAP(x) (x)
#endif

#define _b(x, N) (((x) >> (N*8)) & 0xFF)

BYTE b0(UINT x){
    return x & 0xFF;
}
BYTE b1(UINT x){
    return  (x >> 8) & 0xFF;
}
BYTE  b2(UINT x){
    return  (x >> 16) & 0xFF;
}
BYTE  b3(UINT x) {
    return (x >> 24) & 0xFF;
}
UINT BYTES_TO_UINT(BYTE x0, BYTE x1, BYTE x2, BYTE x3){
    return ((x0 << 24) ^ (x1 << 16) ^ (x2 << 8) ^ x3);
}
UINT BYTEARRAY_TO_UINT(const BYTE *x) {
    return ((x[0] << 24) ^ (x[1] << 16) ^ (x[2] << 8) ^ x[3]);
}

