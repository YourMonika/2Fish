UINT polyMult(UINT a, UINT b)
{
    UINT t=0;
    while (a)
    {
        /*printf("A=%X  B=%X  T=%X\n", a, b, t);*/
        if (a&1) t^=b;
        b <<= 1;
        a >>= 1;
    }
    return t;
}

/* модуль полинова в GF (2**8) */
UINT gfMod(UINT t, UINT modulus)
{
    int i;
    UINT tt;

    modulus <<= 7;
    for (i = 0; i < 8; i++)
    {
        tt = t ^ modulus;
        if (tt < t) t = tt;
        modulus >>= 1;
    }
    return t;
}

/* умножение по модулю в поле */
#define gfMult(a, b, modulus) gfMod(polyMult(a, b), modulus)

